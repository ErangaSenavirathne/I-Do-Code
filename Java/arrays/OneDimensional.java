package arrays;

public class OneDimensional {
    public static void main(String[] args) {
        int[] number = new int[3];
        number[0] = 10;
        number[1] = 20;
        number[2] = 30;

        System.out.println(number[0]);
        System.out.println(number[1]);
        System.out.println(number[2]);
    }
}